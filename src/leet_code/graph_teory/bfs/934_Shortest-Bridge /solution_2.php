class Solution
{
    private $visited;
    private $size;
    private $islands = [];

    /**
     * @param Integer[][] $grid
     * @return Integer
     */
    function shortestBridge($grid)
    {
        $size = count($grid);
        $this->visited = [];
        $this->size = count($grid);

        $index = 0;
        for ($i = 0; $i < $size; $i++) {
            for ($j = 0; $j < $size; $j++) {
                if ($grid[$i][$j] && !($this->visited[$i][$j] ?? false)) {
                    $this->dfs($i, $j, $grid, $index);
                    $index++;
                    break 2;
                }
            }
        }

        $count = [];
        foreach ($this->islands[0] as $terrain) {
            $this->visited = [];

            $count[] = $this->bfs(
                $terrain[0],
                $terrain[1],
                $grid,
//                $this->islands[1]
            );
        }

        return min($count);
    }

    function dfs($x, $y, &$grid, $index)
    {
        if ($x < 0 || $y < 0 || $x >= $this->size || $y >= $this->size) {
            return;
        }

        if (($this->visited[$x][$y] ?? false) || $grid[$x][$y] === 0) {
            return;
        }

        $this->visited[$x][$y] = 1;
        $grid[$x][$y] = 2;
        $this->islands[$index][] = [$x, $y];

        $this->dfs($x + 1, $y, $grid, $index);
        $this->dfs($x - 1, $y, $grid, $index);
        $this->dfs($x, $y + 1, $grid, $index);
        $this->dfs($x, $y - 1, $grid, $index);
    }

    function bfs($x, $y, $grid)
    {
        $queue = [];
        $queue[] = [$x, $y, -1];

        while (!empty($queue)) {
            $node = array_shift($queue);
            $x = $node[0];
            $y = $node[1];
            $count = $node[2];

//            if (in_array([$x, $y], $toIsland)) {
            if ($grid[$x][$y] == 1 && ($this->visited[$x][$y] ?? false) != 2) {
                break;
            }

            if ($x < 0 || $y < 0 || $x >= $this->size || $y >= $this->size) {
                continue;
            }

            if (($this->visited[$x][$y] ?? false)) {
                continue;
            }

            $this->visited[$x][$y] = 1;
            $queue[] = [$x + 1, $y, $count + 1];
            $queue[] = [$x - 1, $y, $count + 1];
            $queue[] = [$x, $y + 1, $count + 1];
            $queue[] = [$x, $y - 1, $count + 1];
        }

        return $count ?? 0;
    }
}


